#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *str;
	char *start;

	if ((str = (char *)malloc(sizeof(char) * size + 1)) == 0)
		return (0);
	start = str;
	while (size--)
		*str++ = '\0';
	*str = '\0';
	return (start);
}
