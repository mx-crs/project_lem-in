#include "libft.h"

void	*ft_recalloc(void *ptr, size_t src_size, size_t new_size)
{
	void *new_ptr;

	if (new_size == 0)
	{
		if (ptr)
			free(ptr);
		ptr = NULL;
		return (ptr);
	}
	if (ptr == NULL)
		return (ft_memalloc(new_size));
	if (new_size <= src_size)
		return (ptr);
	new_ptr = ft_memalloc(new_size);
	if (new_ptr)
	{
		ft_memcpy(new_ptr, ptr, src_size);
		free(ptr);
	}
	return (new_ptr);
}
