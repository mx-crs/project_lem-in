#include "libft.h"

int		ft_count_if(char **tab, int (*f)(char*))
{
	int k;
	int i;

	i = 0;
	k = 0;
	while (tab[i] != 0)
	{
		k += f(tab[i]);
		i++;
	}
	return (k);
}
