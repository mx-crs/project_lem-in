#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;
	int point;

	point = 0;
	i = 2;
	while (i * i <= nb)
	{
		if (i * i == nb)
		{
			point = 1;
			break ;
		}
		i++;
	}
	if (point == 0 || nb <= 0)
		i = 0;
	if (nb == 1)
		i = 1;
	return (i);
}
